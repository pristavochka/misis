#Найти среднее среди положительных элементов матрицы А размером 5 × 7 .
A=[[1, 2, -3, 4, -5, 6, 7],
   [-34, -54, 68, 12, 33, 51, 1],
   [90, -36, 57, 41, 98, 44, -12],
   [49, -58, 97, -83, -30, 99, -51],
   [42, 95, 31, 27, 74, -32, 15]]
n=0
p=0
for i in range (0, 7):
    for j in range (0, 5):
        if A[j][i] > 0 :
            n+= A[j][i]
            p+=1
f=n/p
print(f)